#  Print first 5 characters
text = input("Enter a string: ")
print(text[:5])


# Print last 3 characters
text = input("Enter a string: ")
print(text[-3:])


# Reverse string using slicing
text = input("Enter a string: ")
print(text[::-1])


# Print every 2nd character
text = input("Enter a string: ")
print(text[::2])


# Remove first and last character
text = input("Enter a string: ")
print(text[1:-1])
